let str='shubhangi'
let newst=''
for(let i=str.length-1;i>=0;i--){
   newst=newst+str[i]  
   //           i
   //   i   +   g
   //   ig  +   n
   //   ign +   a
   //   igna  +   h
}
console.log(newst)//ignahbuhs

